package mytmall.pojo;

public class Property
{
    private int id;
    private String name;
    private int cid;

    public void setId(int id)
    {
        this.id = id;
    }
    public int getId()
    {
        return id;
    }

    public void setName(String name)
    {
        this.name = name;
    }
    public String getName()
    {
        return name;
    }

    public void setCid(int cid)
    {
        this.cid = cid;
    }
    public int getCid()
    {
        return cid;
    }
}
